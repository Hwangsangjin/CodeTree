#include <iostream>
using namespace std;

int main()
{
    int A, B;
    cin >> A >> B;

    if (A % 2 == 0)
    {
        cout << "even" << endl;
    }
    else
    {
        cout << "odd" << endl;
    }

    if (B % 2 == 0)
    {
        cout << "even" << endl;
    }
    else
    {
        cout << "odd" << endl;
    }

    return 0;
}